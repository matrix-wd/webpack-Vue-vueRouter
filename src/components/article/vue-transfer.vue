			具体情况就要上官方网站查询或发邮件问学校。像RICE、EMORY、UW、OSU、BU，SAT/ACT不是对international students必须要求的，BROWN则是转学需要看。但总体上来说，如果考一个SAT/ACT，而且成绩不错，可以增加拿奖学金的机会
