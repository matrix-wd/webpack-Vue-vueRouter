			所有学校都希望自己收的转学生是原来学校的最优秀的学生，选择转到他们学校是因为喜欢这个学校，希望在这个学校自己有更好的发展，而不是在原来的学校混得很潦倒而被迫选择逃走的。所以申请材料的核心就是为了向对方学校显示出你正是他们想要的这类学生。
