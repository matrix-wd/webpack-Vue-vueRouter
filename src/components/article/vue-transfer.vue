			<h3>转学申请的成功率</h3>
			<small>一般来说名校的转学生名额是很少的，因此要拿到名校的转学录取难度不亚于新生录取，甚至更高。美国学校对大一和大二转学的名额是不同的，大一到大二的名额要多一些，大二转大三就不多了。
			另外对于大一就转学的学生来说，你的转学的理由会比较难解释，你需要阐述清楚你为什么要转学?是否因为现在的学校你不满意?或者你为什么对原学校不满意?因此，目前本科转学大多还是大二转大三的居多，成功概率也会更高一些。
			美国社区大学“转学”是现在很多同学曲线赴美读名校的途径之一。在美国，转学制度是被任何一所大学所承认的，也就是说，无论你在什么阶段，都可以选择上进。而无论你是通过何种方式进行转学，只要符合条件就会被列入录取的行列。但这条所谓的“捷径”被很多中国学生所误解。
			</small>
			<p>1、充分利用美国的“转学制度”
			近年来，美国名校的录取难度水涨船高。数据显示，美国多所名校的本科录取率再创新低，如哥伦比亚大学的录取率为6.1%，普林斯顿大学录取率为6.99%，斯坦福大学录取率仅有5.05%。若中国的学生直接申请名校，那仅是金字塔上的一小部分学生。对于那些综合成绩GPA不高，或成绩优秀但没参加TOEFL和SAT考试，或其他不具备直接申请美国名牌大学的优秀学生来说，其实可以利用美国的转学制度，来完成自己的美国名校申请目标。
			据美国加州地区某社区大学校方代表介绍，在美国，学生只要在社区学校完成两年的课程安排并获得较高的GPA之后，学生就可以在申请期内直接申请进入四年制的大学，甚至很多美国名校，不需要再提交语言成绩等凭证，与直接申请美国名校获得的学位是一样的。据了解，美国共有1422所社区大学，学制两年，毕业可获得副学士学位，毕业后再转学到四年制的大学可获得学士学位。通过转学的方式，不仅曲线能申请到美国的名校，也会节省很多学费。社区大学每年学费7000美元左右，相当于公立大学学费的三分之一、私立大学学费的五分之一。
			要查看与美国名校是否转学协议
			目前，美国两年社区大学与许多四年制综合性大学之间有转学协议，当社区大学毕业生达到转学协议中的要求（如平均成绩GPA、必修课程等）后，则按照规定保证转入到四年制综合性大学，继续本科第三年学习。入读社区大学后，学生可申请对口的四年制大学，也可申请其他的大学，但没有能入学的保障。如加州有三个层次的学校：加州大学、州立大学、社区大学，学生可选择去州立大学，也可以去加州大学。但如果申请跨州转学，其学分承认度会有所不同。
			学生在选择社区学院时，一方面要了解所入读的社区学校和哪些四年制大学有转校协议。另一方面，要确认社区学校的学分是否会被目标院校所承认。一般来说，在达到本州最低分数要求的前提下，州内社区大学基本可以成功转学分。
			学生在社区学院取得较高的平时成绩，在转学时较有优势，某社区学院校方代表表示，该校90%的社区大学学生二年后会申请四年制大学，成功率达到80%以上。目前该社区大学对接将近十所加州名校，其中大学的最低排名在110名左右，学生只要符合硬件标准，如能够达到3.7以上的GPA要求，就可被这些名校录取。若学生出国的留学目标是转学名校，但直接申请有难度，转而选择通过社区大学再转学名校，那么在选择社区大学时要选择有GPA升学保障计划的学校。
			</p>
			<p>2、优异的GPA成绩是转学成功的基本要求
			美国大学本科的毕业要求一般是完成125个左右的学分，正常学时为四年。学生只要拿足学分，就可以毕业，有些优秀的学生三年甚至两年半就毕业了，而国际学生一般要求五年内毕业。美国大学课程一般分为通识课程、专业课程和选修课程三类，通识课程是所有的专业、所有学校的学生必须要修的课程，一般在前两年完成。很多四年制大学和州内的二年制学院建立了密切的联系，甚至连课程的编号都设置的一样。这样，即使是四年制大学的学生，有时为了省钱或修读更多的学分，也会去二年制大学选修一些课程。所以，这也是学生在美国读大学的前两年很方便带着学分转入另一所学校的原因。
			　　
			在四年制大学，学生一开始就要面临几百人上大课的情况，而社区大学则采用小班教学的模式。一个班大约有二十名左右的学生，这样学生可以和教授有更多的交流机会，并且学校还会安排专门的老师为在学习上有困难的同学提供辅导和帮助，有助于学生尽快适应美国的学习环境并取得优异的成绩。前两年大多是基础课程，名校录取社区大学学生的硬件要求基本就是GPA，如3.5分以上。其他的实践活动也许可以作为辅助加分项目，但不是硬性要求。
			</p>
