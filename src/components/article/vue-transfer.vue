			3、3月份开始申请，申请期三个月
			高中直接申请名校需要GPA、SAT、托福等成绩证明，在进入社区大学两年之后则不需要这些成绩证明。因为学生如果在社区大学能够取得优异的成绩，就说明学生有能力进行后续深入的学习。社区大学适合高考没有取得优异成绩，但是又不甘心的学生，可以通过社区大学，直接顺利进入伯克利等名校。在社区大学既可以和来拿学分的四年制大学学生一起学习，也可以和高中生一起交流当地文化。

			学生入读社区大学1年半后就开始申请大学，这期间的选课是要学生和社区导师讨论确定，除了必修课程之外，还有24个学分的选修课程，这部分就要根据大学志愿来选择课程就读。每所大学对于转学生的要求不同，所以选课很重要，要根据转入大学的要求选择相应的课程。而且好成绩非常重要，GPA高分是一个大的前提。学生要选择大学认可度较高的课程并获得高的GPA，如果某一门功课成绩不够好，就需要选择相似的课程来替代或者覆盖成绩，确保在社区大学的GPA成绩名列前茅。学生需要注意的是，如果错过一年半后的申请或者申请不成功，就需要再等一年。一般申请期限是三个月，在每年的3月份左右。
