			转学生需要提交的成绩单是大学成绩单和高中成绩单。申请转学，GPA是相当重要的。因为这是对方学校判断你在你现在学校的表现好坏的最直观的参考资料。如果你现在是在国内就读，学校本身的排名不会影响很大，因为外国学校对中国学校有了解的就那么几所知名大学。如果你是在美国申请转学，学校的排名也有一定影响，因为美国人毕竟对自己国家的学校还是有一定了解的。如果你的学校排名不理想，那你就要想法设法在班级排名上具有一定优势，因为平时成绩对于转学生是相当重要的，好的成绩从某一方面反映出你是个好学生。
