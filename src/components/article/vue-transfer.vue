			高中老师推荐信在TRANSFER的时候如果写得好非常有用，因为高中老师通常比大学老师更了解学生的情况。对于推荐信重要性的排列顺序如下：有师生关系的名人> 班主任 > 任课老师 > 没关系的名人。
			推荐信不光要强调这个学生在原来学校表现如何如何好，还要强调这个学生虽然在这个学校表现很出色，但是因为他的确很有才华，所以他需要一个更广阔的空间去施展他的才华，所以很支持他选择离开。
